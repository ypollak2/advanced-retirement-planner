// Helper function to search companies
function searchCompanies(query, limit = 50) {
    if (!query) return stockCompanies.slice(0, limit);
    
    const searchTerm = query.toLowerCase();
    const results = stockCompanies.filter(company =>
        company.symbol.toLowerCase().includes(searchTerm) ||
        company.name.toLowerCase().includes(searchTerm) ||
        company.sector.toLowerCase().includes(searchTerm)
    );
    
    return results.slice(0, limit);
}

// Helper function to get companies by sector
function getCompaniesBySector(sector) {
    return stockCompanies.filter(company => 
        company.sector.toLowerCase() === sector.toLowerCase()
    );
}

// Helper function to get all unique sectors
function getAllSectors() {
    const sectors = [...new Set(stockCompanies.map(company => company.sector))];
    return sectors.sort();
}

// Helper function to validate ticker symbol
function isValidTicker(ticker) {
    // Basic validation: 1-5 uppercase letters, may include dots for special shares
    return /^[A-Z]{1,5}(\.[A-Z])?$/.test(ticker);
}

